let game = document.querySelector('.game')

let choosCard = document.querySelectorAll('.choosCard')

let reset = document.querySelector('.reset')
reset.style.display = 'none';

let rand;

let n = 0;



function myFunction(x) {

    reset.style.display = 'block';

    for (let i = 0; i < x; i++) {

        let card = document.createElement('div');

        if (x == 16) {


            rand = Math.floor(Math.random() * imgs.length);

            card.innerHTML = `<img src="${imgs[rand]}" alt="">`;

            imgs.splice(rand, 1);

        }

        else if (x == 24) {
            rand = Math.floor(Math.random() * imgss.length);

            card.innerHTML = `<img src="${imgss[rand]}" alt="">`;

            imgss.splice(rand, 1);
        }



        card.onclick = function () {
            this.classList.add('openCard')

            setTimeout(function () {

                if (document.querySelectorAll('.openCard').length > 1) {

                    
                    n += 1;
                    

                    if (document.querySelectorAll('.openCard')[0].querySelector('img').src
                        == document.querySelectorAll('.openCard')[1].querySelector('img').src) {



                        document.querySelectorAll('.openCard')[0].classList.add('same')
                        document.querySelectorAll('.openCard')[1].classList.add('same')

                        document.querySelectorAll('.openCard')[1].classList.remove('openCard')
                        document.querySelectorAll('.openCard')[0].classList.remove('openCard')


                        localStorage.setItem('attemp', n)

                        if (document.querySelectorAll('.same').length == x) {
                            alert('Complete the game in' + ' '+ localStorage.getItem('attemp') +' '+'try')
                        }



                    }
                    else {
                        document.querySelectorAll('.openCard')[1].classList.remove('openCard')

                        document.querySelectorAll('.openCard')[0].classList.remove('openCard')

                    }
                    
                    


                }

                


            }, 600)

            


        }


        card.classList.add('card');

        

   
        game.appendChild(card);


    }

    let buttons = document.getElementsByClassName('choosCard');


    Array.from(buttons).forEach(element => {
        element.classList.add('hidden')
    });



}







